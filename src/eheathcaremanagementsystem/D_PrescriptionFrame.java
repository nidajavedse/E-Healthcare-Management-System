    private void add_pres_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_pres_ButtonActionPerformed
        // TODO add your handling code here:
        if(id_TF.getText().isEmpty() || Pres_TA.getText().isEmpty() || id_TF.getText().equalsIgnoreCase("Please Enter Id of Patient") ||Pres_TA.getText().equalsIgnoreCase("Please Enter Prescription of Patient here") ){
           JOptionPane.showMessageDialog(this, "Information Missing");
       }
       else{ 
       String pid = id_TF.getText();
        String did = Pres_TA.getText();
        String time =DateTF.getText();
        //using business class
        Prescription p = new Prescription(pid,did,time);
        //third layer
        PresFileHandler pfh = new PresFileHandler();
        pfh.write(p);
        //using business class
        JOptionPane.showMessageDialog(this, "Prescription is added successfully");
        }
    }//GEN-LAST:event_add_pres_ButtonActionPerformed

