    public void displayData(ArrayList <Patient>ap){
         String array[] = new String[6];
         DefaultTableModel tb = (DefaultTableModel)presTable1.getModel();
         for(Patient i: ap){
            array[0] = i.getP_id();
            array[1] = i.getP_name();
            array[2] = i.getP_gender();
            array[3] = i.getP_age()+"";
            array[4] = i.getP_disease();
            array[5] = i.getP_phone_No();
            tb.addRow(array);
        }
        
    }

